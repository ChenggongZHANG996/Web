// È°µÈù¢Âä†ËΩΩÂÆåÊàêÂêéÁöÑÂàùÂßãÂåñ‰ª£Á†Å
document.addEventListener('DOMContentLoaded', function() {
    const loginForm = document.getElementById('loginForm');
    const registerForm = document.getElementById('registerForm');

    if (!loginForm || !registerForm) {
        throw new Error("Required form elements not found");
    }

    // Ê≥®ÂÜåË°®ÂçïÊ≠•È™§ÂàáÊç¢ÈÄªËæë
    const nextButtons = document.querySelectorAll('.next-step');
    const prevButtons = document.querySelectorAll('.prev-step');
    const steps = document.querySelectorAll('.register-step');
    const progressSteps = document.querySelectorAll('.progress-step');

    function updateProgress(currentStep) {
        progressSteps.forEach((step, index) => {
            if (index < currentStep) {
                step.classList.add('active');
            } else {
                step.classList.remove('active');
            }
        });
    }

    function showStep(stepNumber) {
        steps.forEach(step => {
            step.classList.remove('active');
            if (step.dataset.step === stepNumber.toString()) {
                step.classList.add('active');
            }
        });
        updateProgress(stepNumber);
        writeLog(`Showing registration step ${stepNumber}`, "NAVIGATION");
    }

    // Ê∑ªÂä†ÂØÜÁ†ÅÈ™åËØÅÂáΩÊï∞
    function validatePassword(password) {
        // Ëá≥Â∞ë8‰∏™Â≠óÁ¨¶ÔºåÂåÖÂê´Â§ßÂ∞èÂÜôÂ≠óÊØç„ÄÅÊï∞Â≠óÂíåÁâπÊÆäÂ≠óÁ¨¶
        const minLength = password.length >= 8;
        const hasUpperCase = /[A-Z]/.test(password);
        const hasLowerCase = /[a-z]/.test(password);
        const hasNumbers = /\d/.test(password);
        const hasSpecialChar = /[!@#$%^&*(),.?":{}|<>]/.test(password);

        return {
            isValid: minLength && hasUpperCase && hasLowerCase && hasNumbers && hasSpecialChar,
            errors: {
                minLength: !minLength,
                hasUpperCase: !hasUpperCase,
                hasLowerCase: !hasLowerCase,
                hasNumbers: !hasNumbers,
                hasSpecialChar: !hasSpecialChar
            }
        };
    }

    // Ê∑ªÂä†ÈÇÆÁÆ±È™åËØÅÂáΩÊï∞
    function validateEmail(email) {
        const emailRegex = /^[^\s@]+@[^\s@]+\.[^\s@]+$/;
        return emailRegex.test(email);
    }

    // Ê∑ªÂä†ÁîµËØùÂè∑Á†ÅÈ™åËØÅÂáΩÊï∞
    function validatePhone(phone) {
        // Ê≥ïÂõΩÊâãÊú∫Âè∑Ê†ºÂºè
        const phoneRegex = /^(?:(?:\+|00)33|0)\s*[1-9](?:[\s.-]*\d{2}){4}$/;
        return phoneRegex.test(phone);
    }

    // ‰øÆÊîπvalidateStepÂáΩÊï∞
    function validateStep(step) {
        const inputs = step.querySelectorAll('input[required]');
        let isValid = true;
        let errors = [];
        
        inputs.forEach(input => {
            let fieldValid = true;
            const value = input.value.trim();

            if (!value) {
                fieldValid = false;
                errors.push(`Le champ ${input.name} est requis`);
            } else {
                // ÁâπÂÆöÂ≠óÊÆµÁöÑÈ™åËØÅ
                switch(input.name) {
                    case 'email':
                        if (!validateEmail(value)) {
                            fieldValid = false;
                            errors.push("Format d'email invalide");
                        }
                        break;
                    case 'phone':
                        if (!validatePhone(value)) {
                            fieldValid = false;
                            errors.push("Format de num√©ro de t√©l√©phone invalide");
                        }
                        break;
                    case 'password':
                        const passwordValidation = validatePassword(value);
                        if (!passwordValidation.isValid) {
                            fieldValid = false;
                            if (passwordValidation.errors.minLength) errors.push("Le mot de passe doit contenir au moins 8 caract√®res");
                            if (passwordValidation.errors.hasUpperCase) errors.push("Le mot de passe doit contenir au moins une majuscule");
                            if (passwordValidation.errors.hasLowerCase) errors.push("Le mot de passe doit contenir au moins une minuscule");
                            if (passwordValidation.errors.hasNumbers) errors.push("Le mot de passe doit contenir au moins un chiffre");
                            if (passwordValidation.errors.hasSpecialChar) errors.push("Le mot de passe doit contenir au moins un caract√®re sp√©cial");
                        }
                        break;
                    case 'confirm_password':
                        const password = step.querySelector('input[name="password"]').value;
                        if (value !== password) {
                            fieldValid = false;
                            errors.push("Les mots de passe ne correspondent pas");
                        }
                        break;
                }
            }

            if (!fieldValid) {
                isValid = false;
                input.classList.add('error');
                writeLog(`Validation failed for field: ${input.name}`, "VALIDATION");
            } else {
                input.classList.remove('error');
            }
        });

        if (!isValid) {
            // ÊòæÁ§∫ÈîôËØØÊ∂àÊÅØ
            showErrors(errors);
        }

        return isValid;
    }

    // Ê∑ªÂä†ÈîôËØØÊòæÁ§∫ÂáΩÊï∞
    function showErrors(errors) {
        // ÁßªÈô§Áé∞ÊúâÁöÑÈîôËØØÊ∂àÊÅØ
        const existingErrors = document.querySelectorAll('.error-message');
        existingErrors.forEach(error => error.remove());

        // ÂàõÂª∫Êñ∞ÁöÑÈîôËØØÊ∂àÊÅØÂÆπÂô®
        const errorContainer = document.createElement('div');
        errorContainer.className = 'error-message';
        errorContainer.style.color = 'red';
        errorContainer.style.marginTop = '10px';
        errorContainer.style.fontSize = '14px';

        errors.forEach(error => {
            const errorElement = document.createElement('p');
            errorElement.textContent = error;
            errorContainer.appendChild(errorElement);
        });

        // Ê∑ªÂä†Âà∞Ë°®Âçï‰∏≠
        const currentStep = document.querySelector('.register-step.active');
        currentStep.appendChild(errorContainer);
    }

    // ‰∏ã‰∏ÄÊ≠•ÊåâÈíÆÁÇπÂáª‰∫ã‰ª∂
    nextButtons.forEach(button => {
        button.addEventListener('click', function(e) {
            e.preventDefault();
            const currentStep = this.closest('.register-step');
            const currentStepNumber = parseInt(currentStep.dataset.step);
            
            if (validateStep(currentStep)) {
                if (currentStepNumber < 3) {
                    showStep(currentStepNumber + 1);
                    writeLog(`Moving to step ${currentStepNumber + 1}`, "NAVIGATION");
                }
            } else {
                writeLog("Step validation failed", "ERROR");
            }
        });
    });

    // ‰∏ä‰∏ÄÊ≠•ÊåâÈíÆÁÇπÂáª‰∫ã‰ª∂
    prevButtons.forEach(button => {
        button.addEventListener('click', function(e) {
            e.preventDefault();
            const currentStep = this.closest('.register-step');
            const currentStepNumber = parseInt(currentStep.dataset.step);
            
            if (currentStepNumber > 1) {
                showStep(currentStepNumber - 1);
                writeLog(`Moving back to step ${currentStepNumber - 1}`, "NAVIGATION");
            }
        });
    });

    // ÂàùÂßãÂåñÊòæÁ§∫Á¨¨‰∏ÄÊ≠•
    showStep(1);

    // Ê∑ªÂä†ÊåâÈíÆÁÇπÂáª‰∫ã‰ª∂ÁõëÂê¨
    const loginBtn = document.getElementById('loginBtn');
    const registerBtn = document.getElementById('registerBtn');

    if (loginBtn) {
        loginBtn.addEventListener('click', showLogin);
    }
    if (registerBtn) {
        registerBtn.addEventListener('click', showRegister);
    }

    // ÁôªÂΩïË°®ÂçïÊèê‰∫§Â§ÑÁêÜ
    loginForm.addEventListener("submit", async function (e) {
        e.preventDefault();
        
        const email = document.getElementById("loginEmail").value;
        const password = document.getElementById("loginPassword").value;
        const userType = document.querySelector('input[name="loginUserType"]:checked').value;

        try {
            const { data: { user }, error } = await supabaseClient.auth.signInWithPassword({
                email: email,
                password: password,
            });

            if (error) throw error;

            // Ëé∑ÂèñÁî®Êà∑Á±ªÂûã
            const { data: userData, error: userError } = await supabaseClient
                .from('users')
                .select('user_type')
                .eq('id', user.id)
                .single();

            if (userError) throw userError;

            // ‰øùÂ≠òÂÆåÊï¥ÁöÑÁî®Êà∑‰ø°ÊÅØÂà∞Êú¨Âú∞Â≠òÂÇ®
            localStorage.setItem('user_session', JSON.stringify({
                user: {
                    ...user,
                    user_type: userData.user_type
                }
            }));

            // Ê∑ªÂä†Êó•Âøó
            console.log("User type:", userType);
            console.log("Base URL:", baseUrl);

            // Ë∑≥ËΩ¨ÂâçÊâìÂç∞ÂÆåÊï¥URL
            const profUrl = `${baseUrl}App_Professeur/HTML/Prof_index.html`;
            console.log("Redirecting to:", profUrl);

            if (userType === 'tutor') {
                window.location.href = profUrl;
            } else {
                window.location.href = `${baseUrl}App_Etudiant/HTML/Student_index.html`;
            }

            writeLog("Connexion r√©ussie", "success");
        } catch (error) {
            writeLog(`Erreur de connexion: ${error.message}`, "error");
            showError(error.message);
        }
    });

    // Ê≥®ÂÜåË°®ÂçïÊèê‰∫§Â§ÑÁêÜ
    registerForm.addEventListener('submit', async function(e) {
        e.preventDefault();
        writeLog("Starting registration process", "AUTH");

        // Êî∂ÈõÜÊâÄÊúâË°®ÂçïÊï∞ÊçÆ
        const formData = {
            user_type: document.querySelector('input[name="user_type"]:checked').value,
            first_name: document.getElementById('first_name').value,
            last_name: document.getElementById('last_name').value,
            email: document.getElementById('email').value,
            phone: document.getElementById('phone').value,
            password: document.getElementById('password').value
        };

        try {
            // 1. ÂàõÂª∫ËÆ§ËØÅÁî®Êà∑
            const { data: { user }, error: authError } = await supabaseClient.auth.signUp({
                email: formData.email,
                password: formData.password,
                options: {
                    data: {
                        first_name: formData.first_name,
                        last_name: formData.last_name,
                        user_type: formData.user_type
                    }
                }
            });

            if (authError) throw authError;

            // 2. ÂàõÂª∫Áî®Êà∑ÈÖçÁΩÆÊñá‰ª∂
            const { error: profileError } = await supabaseClient
                .from('users')
                .insert([
                    {
                        id: user.id,
                        first_name: formData.first_name,
                        last_name: formData.last_name,
                        email: formData.email,
                        phone: formData.phone,
                        user_type: formData.user_type
                    }
                ]);

            if (profileError) throw profileError;

            writeLog("Registration successful", "SUCCESS");
            
            // ÊòæÁ§∫ÊàêÂäüÊ∂àÊÅØ
            alert("Inscription r√©ussie ! Veuillez v√©rifier votre email pour confirmer votre compte.");
            
            // ÈáçÂÆöÂêëÂà∞ÁôªÂΩïÈ°µÈù¢
            showLogin();

        } catch (error) {
            writeLog(`Registration error: ${error.message}`, "ERROR");
            showErrors([`Erreur d'inscription: ${error.message}`]);
        }
    });
});

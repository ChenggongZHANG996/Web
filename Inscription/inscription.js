/**
 * Â¢ûÂº∫ÁöÑÊó•ÂøóËÆ∞ÂΩïÂáΩÊï∞
 * @param {string} message - Êó•ÂøóÊ∂àÊÅØ
 * @param {string} type - Êó•ÂøóÁ±ªÂûãÔºàÂØπÂ∫î LOG_STYLES ÁöÑÈîÆÔºâ
 * @param {Object} [data] - ÂèØÈÄâÁöÑÈôÑÂä†Êï∞ÊçÆ
 */
function writeLog(message, type = "SYSTEM", data = null) {
  const timestamp = new Date().toISOString();
  const icon = LOG_ICONS[type] || "‚ÑπÔ∏è";
  const style = LOG_STYLES[type] || LOG_STYLES.SYSTEM;

  // Âü∫Á°ÄÊó•Âøó‰ø°ÊÅØ
  const logPrefix = `${icon} [${timestamp}] [${type}]`;

  // ÊéßÂà∂Âè∞ËæìÂá∫
  if (data) {
    console.groupCollapsed(`%c${logPrefix} ${message}`, style);
    console.log("ËØ¶ÁªÜÊï∞ÊçÆ:", data);
    console.groupEnd();
  } else {
    console.log(`%c${logPrefix} ${message}`, style);
  }

  // Â≠òÂÇ®Âà∞ localStorage
  try {
    const logEntry = {
      timestamp,
      type,
      message,
      data,
    };

    // ÊåâÁ±ªÂûãÂàÜÁ±ªÂ≠òÂÇ®
    const logKey = `logs_${type.toLowerCase()}`;
    const logs = JSON.parse(localStorage.getItem(logKey) || "[]");
    logs.push(logEntry);

    // ÈôêÂà∂ÊØèÁßçÁ±ªÂûãÊúÄÂ§öÂ≠òÂÇ®100Êù°Êó•Âøó
    if (logs.length > 100) {
      logs.shift();
    }

    localStorage.setItem(logKey, JSON.stringify(logs));

    // ÈîôËØØÊó•ÂøóÈ¢ùÂ§ñÂ≠òÂÇ®
    if (type === "ERROR") {
      const errorLogs = JSON.parse(localStorage.getItem("error_logs") || "[]");
      errorLogs.push(logEntry);
      if (errorLogs.length > 50) {
        errorLogs.shift();
      }
      localStorage.setItem("error_logs", JSON.stringify(errorLogs));
    }
  } catch (error) {
    console.error("Êó•ÂøóÂ≠òÂÇ®Â§±Ë¥•:", error);
  }
}

